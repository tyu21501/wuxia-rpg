/**
 * 生成行動敘事
 * @param {string} action - 玩家行動文字
 * @param {object} state  - 完整遊戲狀態
 * @returns {string}      - 含《狀態》區塊的敘事文字
 */
export function generateNarrative(action, state) {
  const loc    = state.world?.location || '青石鎮';
  const spread = state.world?.anomaly_spread || 0;
  const sanity = state.player?.sanity || 100;
  const day    = state.world?.day || 1;
  const time   = state.world?.time || '傍晚';
  const identity = state.player?.identity || 'none';
  const rep    = state.player?.reputation || {};
  const facts  = (state.world?.known_facts || []).length;
  const identityLabel = { none:'旅人', jianghu:'江湖俠客', containment:'收容者', anomaly:'異常者' }[identity] || '旅人';

  const actionType = classifyAction(action);
  const pool = LOCATION_NARRATIVES[loc];

  let narrative = '';

  // 從地點敘事庫選取（使用防重複機制）
  if (pool) {
    const candidates = pool[actionType] || pool.explore || [];
    if (candidates.length > 0) {
      const cacheKey = `${loc}:${actionType}`;
      narrative = pickNonRepeat(candidates, cacheKey);
    }
  }

  // 若無匹配，生成通用敘事
  if (!narrative) {
    narrative = generateGenericNarrative(action, loc, spread, sanity, day, time, identityLabel, actionType, facts);
  }

  // 添加狀態情境後綴
  const contextSuffix = generateContextSuffix(spread, sanity, rep, day);
  if (contextSuffix && !narrative.includes('《狀態》')) {
    narrative += contextSuffix;
  }

  return `【${loc}】\n\n${narrative}`;
}

/**
 * 生成 NPC 對話敘事（優先使用角色專屬對話庫，按信任度分層）
 */
export function generateNpcDialogue(npcId, npc, message, state) {
  const loc    = state.world?.location || '青石鎮';
  const spread = state.world?.anomaly_spread || 0;
  const trust  = npc?.trust ?? 0;
  const npcDef = NPC_DIALOGUES[npcId];
  const cacheKey = `npc:${npcId}`;

  // ── 無名歸人（三層信任度）──
  if (npcId === 'unnamed_survivor' && npcDef) {
    const pool = trust >= 50 ? npcDef.trust_high
               : trust >= 20 ? npcDef.trust_mid
               : npcDef.trust_low;
    if (pool?.length) return `【${loc} · 與無名歸人交談】\n\n${pickNonRepeat(pool, cacheKey)}`;
  }

  // ── 玄真道人（謎語式提示）──
  if (npcId === 'taoist' && npcDef?.riddles?.length) {
    const riddle = pickNonRepeat(npcDef.riddles, cacheKey);
    return `【${loc} · 與玄真道人交談】\n\n${riddle.answer}`;
  }

  // ── 殘兵張三（碎片式情報，三組各三條）──
  if (npcId === 'wounded_soldier' && npcDef?.intel_fragments?.length) {
    const frag = pickNonRepeat(npcDef.intel_fragments, cacheKey + ':group');
    if (frag?.lines?.length) {
      const line = pickNonRepeat(frag.lines, cacheKey + ':line');
      return `【${loc} · 與殘兵張三交談】\n\n${line}`;
    }
  }

  // ── 無憶孩兒（主線 + 數字線索）──
  if (npcId === 'amnesiac_child' && npcDef) {
    const all = [...(npcDef.main_lines || [])];
    if (all.length) return `【${loc} · 與無憶孩兒交談】\n\n${pickNonRepeat(all, cacheKey)}`;
  }

  // ── 其他 NPC（lines 陣列）──
  if (npcDef?.lines?.length) {
    return `【${loc} · 與${npc.name}交談】\n\n${pickNonRepeat(npcDef.lines, cacheKey)}`;
  }

  // ── 通用備用回應 ──
  return `【${loc} · 與${npc.name}交談】\n\n${generateGenericNpcLine(npc, loc, spread, state)}`;
}

// ── 通用敘事生成 ──────────────────────────────────────────────
function generateGenericNarrative(action, loc, spread, sanity, day, time, identityLabel, actionType, facts) {
  const cleanAction = action.replace(/^【[^】]*】\s*/, '').replace(/^(我|你)/, '').slice(0, 40);

  // 依行動類型選對應開場語
  const actionOpeners = {
    investigate: ['你仔細地', '你低下頭，認真地', '帶著調查者的眼神，你'],
    talk:        ['你開口問道，', '你找到一個看起來知情的人，', '你壓低聲音，試探性地'],
    contain:     ['你取出準備好的材料，', '依照所學的方法，你', '你深吸一口氣，開始'],
    meditate:    ['你盤腿坐下，閉上眼睛，', '你調整呼吸，讓心神沉靜，', '在這片異常的土地上，你嘗試'],
    rest:        ['你找了個相對安全的角落，', '身心俱疲，你決定', '你靠牆坐下，'],
    combat:      ['你迅速反應，', '危機瞬間，你的身體先於意識做出反應，', '你拉開架勢，'],
    observe:     ['你退到暗處，靜靜地', '你沒有採取行動，只是', '你放慢步伐，讓自己融入環境，'],
    flee:        ['你迅速撤離了', '你選擇了暫時撤退，', '你的直覺告訴你不對，你'],
    help:        ['你停下腳步，伸出了援手。', '你沒有路過，而是留下來，', '你放下了自己的事，'],
    gather_info: ['你在鎮民之間周旋，', '你以閒談的方式打聽，', '你努力拼湊著各方說法，'],
    read:        ['你翻開眼前的文字，', '你仔細辨認那些字跡，', '你把燈湊近，'],
    explore:     ['你四處走動，', '你沒有特定目標，只是漫步，', '你讓腳步帶著你，'],
  };

  const openerPool = actionOpeners[actionType] || actionOpeners.explore;
  const opener = pick(openerPool);

  const atmosHigh = [
    '動作的中途，你察覺到某種不對——不是危險，是現實本身有什麼地方歪了，像是一幅畫被人在乾透前用手抹了一下。',
    '空氣凝滯在你周圍，光線以一種不合常理的角度折射。你繼續行動，但你的影子沒有跟上。',
    '你做了這件事，而那件事也在某個地方，以某種方式，做了你。',
  ];
  const atmosMid  = [
    '風從某個方向吹來，帶著一種說不清楚的腐敗氣息，不是動物，不是植物，更像是某種概念在腐爛。',
    '牆角有陰影在移動，但光源沒有改變。你告訴自己那是眼睛疲勞，但你知道自己不相信這個解釋。',
    '你聽見有人很輕地喊了你的名字，轉身，什麼也沒有——只有你剛才站過的地方，地面稍微暖了一點。',
  ];
  const atmosLow  = [
    '這地方的空氣比外面重，你說不出為什麼，只是覺得每一口呼吸都帶著某種額外的重量。',
    '你注意到這裡的鳥不唱歌。不是靜止，牠們在飛，在覓食，只是沒有聲音，好像聲音在某個地方停住了。',
    '事情進展得比你預期的還要順利——這讓你反而感到不安，因為在這個地方，太順利的事情往往意味著有什麼你還沒看到的代價。',
  ];

  const atmos = spread >= 50 ? pick(atmosHigh) : spread >= 25 ? pick(atmosMid) : pick(atmosLow);
  const sanityNote = sanity < 30
    ? '\n\n你的思緒開始不受控地飄移——某些記憶的邊緣變得模糊，某些你確定發生過的事情開始讓你懷疑它是否真的存在。'
    : sanity < 60
    ? '\n\n你強迫自己集中注意力，但某種隱隱約約的不安始終像一根針，不痛但一直存在。'
    : '';

  let stateBlock = '';
  if (actionType === 'investigate') {
    stateBlock = `\n\n《狀態》\n${facts < 3 ? '收容聲望:3' : '收容聲望:5\n理智變化:-3'}\n《結束》`;
  } else if (actionType === 'explore' && spread > 30) {
    stateBlock = `\n\n《狀態》\n理智變化:-3\n《結束》`;
  } else if (actionType === 'meditate') {
    stateBlock = `\n\n《狀態》\n理智變化:5\n收容聲望:2\n《結束》`;
  } else if (actionType === 'rest') {
    stateBlock = `\n\n《狀態》\n理智變化:3\n《結束》`;
  } else if (actionType === 'contain') {
    stateBlock = `\n\n《狀態》\n收容聲望:8\n異常變化:-2\n《結束》`;
  } else if (actionType === 'help') {
    stateBlock = `\n\n《狀態》\n江湖聲望:2\n穩定變化:1\n《結束》`;
  } else if (actionType === 'gather_info') {
    stateBlock = `\n\n《狀態》\n收容聲望:3\n江湖聲望:2\n《結束》`;
  } else if (actionType === 'talk') {
    stateBlock = `\n\n《狀態》\n收容聲望:2\n《結束》`;
  }

  return `${opener}${cleanAction}。\n\n${atmos}${sanityNote}${stateBlock}`;
}

function generateGenericNpcLine(npc, loc, spread, state) {
  const name  = npc.name;
  const trust = npc.trust || 0;
  const fear  = npc.fear  || 0;

  // 高恐懼：幾乎無法溝通
  if (fear > 60) {
    const lines = [
      `${name}見你走近便向後退了幾步，雙手緊握，語氣顫抖：「你……你問我什麼我都不知道。你最好也假裝不知道。」`,
      `${name}的眼神在你臉上停了一秒，然後迅速看向別處，嘴唇動了動，什麼也沒說出來。那種沉默，比任何回答都更讓人不安。`,
    ];
    return pick(lines);
  }

  // 信任極低：戒備
  if (trust < -10) {
    const lines = [
      `${name}打量你的眼神不善：「你是誰？從哪裡來的？這種時候，外來的人都得說清楚自己的來歷。」說完便不再多言。`,
      `${name}沒有正眼看你，語氣敷衍：「我很忙，沒空閒聊。你要打聽什麼，去問別人。」`,
    ];
    return pick(lines);
  }

  // 信任較高：願意透露
  if (trust > 40) {
    const lines = [
      `${name}左右確認四周無人，壓低聲音：「你真的想知道？那件事……算了，跟你說也無妨。但你得答應我，不管聽到什麼，面上都不能露出來。」\n\n《狀態》\n信任變化:5\n收容聲望:3\n《結束》`,
      `${name}長嘆一口氣，似乎下了什麼決定：「我一直想找個人說這件事，但不敢說。你……你看起來不一樣。好，我說，你聽。」\n\n《狀態》\n信任變化:8\n收容聲望:5\n《結束》`,
    ];
    return pick(lines);
  }

  // 中立：猶豫透露
  const lines = [
    `${name}猶豫了一下，說了幾句模稜兩可的話，迴避了你真正想問的核心。但從他說話時的停頓裡，你確定他知道的比說出來的多得多。\n\n《狀態》\n信任變化:2\n《結束》`,
    `${name}答了你的問題，但只答了一半。另一半，像是被什麼擋住了——不像是不知道，更像是不允許說。\n\n《狀態》\n信任變化:3\n《結束》`,
    `（${name}看了你許久）「你問這個，是因為你也發現了什麼，對嗎？」他沒有等你回答，只是點了點頭，好像已經得到了他想要的確認。\n\n《狀態》\n信任變化:4\n收容聲望:2\n《結束》`,
  ];
  return pick(lines);
}

function generateContextSuffix(spread, sanity, rep, day) {
  if (spread >= 70) return `\n\n《狀態》\n異常變化:1\n理智變化:-2\n《結束》`;
  if (spread >= 40) return `\n\n《狀態》\n異常變化:1\n《結束》`;
  return '';
}

/**
 * 解析《狀態》區塊並應用到遊戲狀態
 */
export function parseAndApplyStateBlock(raw, state) {
  const match = raw.match(/《狀態》([\s\S]*?)《結束》/);
  if (!match) return raw;

  const block = match[1];
  const p = state.player;
  const w = state.world;
  const get = (key) => { const m = block.match(new RegExp(key + ':(-?[\\d]+)')); return m ? parseInt(m[1]) : 0; };
  const getStr = (key) => { const m = block.match(new RegExp(key + ':([^\\s《》\\n]+)')); return m ? m[1] : ''; };

  const sd = get('理智變化');  if (sd) p.sanity = Math.max(0, Math.min(p.maxSanity, p.sanity + sd));
  const ad = get('異常變化');  if (ad) w.anomaly_spread = Math.min(100, Math.max(0, w.anomaly_spread + ad));
  const td = get('穩定變化');  if (td) w.town_stability = Math.min(100, Math.max(0, w.town_stability + td));
  const rj = get('江湖聲望');  if (rj) p.reputation.jianghu    = Math.max(0, p.reputation.jianghu    + rj);
  const rc = get('收容聲望');  if (rc) p.reputation.containment = Math.max(0, p.reputation.containment + rc);
  const ra = get('異常聲望');  if (ra) p.reputation.anomaly     = Math.max(0, p.reputation.anomaly     + ra);

  // 線索追蹤
  const clue = getStr('線索');
  if (clue && !w.known_facts.includes(clue)) {
    w.known_facts.push(clue);
  }

  // NPC 信任度（由 npc/talk 端點另外處理，這裡做備援）
  const trust = get('信任變化');
  if (trust && state._current_npc) {
    const npc = state.npcs[state._current_npc];
    if (npc) npc.trust = Math.max(-100, Math.min(100, (npc.trust || 0) + trust));
  }

  // 身份設定
  const identity = getStr('身份');
  if (identity && ['jianghu','containment','anomaly'].includes(identity)) {
    p.identity = identity;
    w.events.push(`【身份確立】${p.name}成為了${{jianghu:'江湖俠客',containment:'收容者',anomaly:'異常接觸者'}[identity]}`);
  }

  // 清除《狀態》區塊，只留下純敘事文字
  return raw.replace(/\n\n《狀態》[\s\S]*?《結束》/g, '').trim();
}

